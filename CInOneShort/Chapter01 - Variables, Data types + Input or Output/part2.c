#include<stdio.h>


int main() {
    // * 1. Integers
    int age = 19;
    printf("age is %d\n",age);

    // * 2. Real Number
    float pi = 3.14;
    printf("value of pi is %f\n",pi);

    // * 3. Characters
    char star = '*';
    printf("star looks like this %c\n", star);

}

/*
Output :
age is 19
value of pi is 3.140000
star looks like this *
*/