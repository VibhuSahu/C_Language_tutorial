// # Type Conversion 
// -> int    oprator    int   ----> int 
// -> int    oprator    float ----> float 
// -> float  oprator    float ----> float 
#include<stdio.h>


int main() {
    printf("int to int Opration     :- %d\n", 2 * 2);
    printf("int to float Opration   :- %f\n", 2 * 3.2);
    printf("float to float Opration :- %f\n", 2.1 * 3.2);

    return 0;
}

/*
Output :
int to int Opration     :- 4
int to float Opration   :- 6.400000
float to float Opration :- 6.720000

*/